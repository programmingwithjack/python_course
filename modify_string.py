# upper
a = "Hello, World!"
print(a.upper())
#lower
a = "Hello, World!"
print(a.lower())
#remove white space
a = "Hello, World!"
print(a.strip())
# replace
a = "Hello, World!"
print(a.replace("H", "J"))
# split
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
#concatenate
a = "Hello"
b = "World"
c = a + b
print(c)
